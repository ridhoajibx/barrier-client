// SLICER
export const reportSlice = createSlice({
  name: "report",
  initialState,
  reducers: {
    // leave this empty here
    resetReports(state) {
      state.reports = {};
      state.pending = false;
      state.error = false;
      state.message = "";
    },

    resetReport(state) {
      state.report = {};
      state.pending = false;
      state.error = false;
      state.message = "";
    },
  },
  // The `extraReducers` field lets the slice handle actions defined elsewhere, including actions generated by createAsyncThunk or in other slices.
  // Since this is an API call we have 3 possible outcomes: pending, fulfilled and rejected. We have made allocations for all 3 outcomes.
  // Doing this is good practice as we can tap into the status of the API call and give our rfids an idea of what's happening in the background.
  extraReducers: (builder) => {
    builder
      // get-reports
      .addCase(getReports.pending, (state) => {
        return {
          ...state,
          pending: true,
        };
      })
      .addCase(getReports.fulfilled, (state, { payload }) => {
        return {
          ...state,
          pending: false,
          error: false,
          reports: payload,
        };
      })
      .addCase(getReports.rejected, (state, { error }) => {
        state.pending = false;
        state.error = true;
        state.message = error.message;
      })

      // get-report-by-id
      .addCase(getReportById.pending, (state) => {
        return {
          ...state,
          pending: true,
        };
      })
      .addCase(getReportById.fulfilled, (state, { payload }) => {
        return {
          ...state,
          pending: false,
          error: false,
          report: payload,
        };
      })
      .addCase(getReportById.rejected, (state, { error }) => {
        state.pending = false;
        state.error = true;
        state.message = error.message;
      })

      .addMatcher(isRejectedAction, (state, action) => {})
      .addDefaultCase((state, action) => {
        let base = {
          ...state,
          ...action.state,
        };
        return base;
      });
  },
});
// SLICER

